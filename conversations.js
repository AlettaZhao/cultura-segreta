// ─── Helper: build chat list for a city ───
export function charactersInCity(city) {
  return Object.entries(CHARACTERS)
    .filter(([_, c]) => c.city === city)
    .map(([id, c]) => ({ id, ...c }));
}

// ─── Helper: get first text preview for chat list ───
export function getPreview(charId) {
  const convo = CONVERSATIONS[charId] || [];
  for (const msg of convo) {
    if (msg.t === 'text' && !msg.mine) return msg.text;
  }
  return '...';
}
